def get_icon(
    name: str,
    size: int = 20,
    cls: str = '',
    aria_hidden: bool = True,
) -> str:
    """
    Retorna um SVG inline para o ícone solicitado.

    Args:
        name:        slug do ícone (ex: 'map-pin', 'paw', 'trash')
        size:        dimensão em px (largura = altura)
        cls:         classe CSS adicional
        aria_hidden: True para ícones decorativos (padrão)
    """
    paths = _PATHS.get(name, _PATHS['info'])
    aria_attr = 'aria-hidden="true"' if aria_hidden else ''
    class_attr = f'class="icon {cls}"' if cls else 'class="icon"'

    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'viewBox="0 0 24 24" '
        f'width="{size}" height="{size}" '
        f'fill="none" '
        f'stroke="currentColor" '
        f'stroke-width="1.5" '
        f'{class_attr} {aria_attr} '
        f'style="display:inline-block;vertical-align:middle;flex-shrink:0;">'
        f'{paths}'
        f'</svg>'
    )
